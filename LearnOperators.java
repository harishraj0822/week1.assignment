package week1.day1;

public class LearnOperators {
public static void main(String[] args) {
	int a = 5;
	int b = 9;
	System.out.println(a+b);
	System.out.println(a-b);
	
	int d =5;
	d+=5;
	System.out.println(d);
	
	int c = 6;
	System.out.println(++c);
	System.out.println(c++);
	System.out.println(c--);
	
	int age = 10;
	if (age>20) { //True
		System.out.println("Allowed");
	}else { //false
	System.out.println("Not Allowed");
	 }

	
  }
}
